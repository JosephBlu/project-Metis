			<h2>Use Case</h2>
			<p>Approaching a busy season, Steven needs to place an order for some more slabs of granite/quartz but needs to find out exactly what he has in stock. so he clicks on to Metis and heads to the inventory page where it displays the current amount of Granite/quartz and the group or bundle they belong to. he know has the number and name of granite/quartz he needs to order.</p>
			<p>Steven is planning out his scheduale. He needs to find out the progress of each work order and how far along the employee is in the fabrication process. so steven logs into Metis and clicks on to the work order and sees listed each work order and the current stage. clicks on one work order and it shows the time and date each stage was completed and the initials of the employee that finished the stage along with a note from the employee. </p>

